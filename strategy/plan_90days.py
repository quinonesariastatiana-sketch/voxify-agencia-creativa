def get_current_phase(start_date_str: str = "2026-06-13") -> int:
    from datetime import date
    start = date.fromisoformat(start_date_str)
    today = date.today()
    days_elapsed = (today - start).days
    if days_elapsed < 30:
        return 1
    elif days_elapsed < 60:
        return 2
    else:
        return 3
